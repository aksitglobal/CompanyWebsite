<script>
document.addEventListener('DOMContentLoaded', function () {
    const overlay = document.getElementById('projectModalOverlay');
    const openBtn = document.getElementById('openProjectModal');
    const closeBtn = document.getElementById('closeProjectModal');
    const form = document.getElementById('projectForm');

    // Open modal
    openBtn.addEventListener('click', function (e) {
        e.preventDefault();
        overlay.classList.add('active');
        document.body.style.overflow = 'hidden';
    });

    // Close modal
    function closeModal() {
        overlay.classList.remove('active');
        document.body.style.overflow = '';
    }

    closeBtn.addEventListener('click', closeModal);

    // Close on overlay click
    overlay.addEventListener('click', function (e) {
        if (e.target === overlay) closeModal();
    });

    // Close on Escape key
    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape' && overlay.classList.contains('active')) closeModal();
    });

    // Form submission → Save to DB then WhatsApp
    form.addEventListener('submit', function (e) {
        e.preventDefault();

        const name = document.getElementById('projectName').value.trim();
        const email = document.getElementById('projectEmail').value.trim();
        const phone = document.getElementById('projectPhone').value.trim();
        const type = document.getElementById('projectType').value;
        const description = document.getElementById('projectDescription').value.trim();

        const message =
            `Hello AKSIT Global!\n` +
            `Name: ${name}\n` +
            `Email: ${email}\n` +
            `Phone: ${phone}\n` +
            `Project Type: ${type}\n` +
            `Description: ${description}`;

        const whatsappUrl = `https://wa.me/923000311868?text=${encodeURIComponent(message)}`;

        // Save to database via AJAX first, then open WhatsApp
        fetch('{{ route("whatsapp-query.store") }}', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content'),
                'Accept': 'application/json'
            },
            body: JSON.stringify({
                name: name,
                email: email,
                phone: phone,
                project_type: type,
                description: description
            })
        })
        .then(response => response.json())
        .then(data => {
            window.open(whatsappUrl, '_blank');
            form.reset();
            closeModal();
        })
        .catch(error => {
            console.error('Error saving query:', error);
            // Still open WhatsApp even if save fails
            window.open(whatsappUrl, '_blank');
            form.reset();
            closeModal();
        });
    });
});
</script>
